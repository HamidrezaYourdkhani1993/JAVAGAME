package com.myjavagame.model;

/**
 * Represents the status of a game.
 */
public enum GameStatus {
    /**
     * Indicates that the game is new and has not yet started.
     */
    NEW,

    /**
     * Indicates that the game is in progress.
     */
    IN_PROGRESS,

    /**
     * Indicates that the game has finished.
     */
    FINISHED
}
